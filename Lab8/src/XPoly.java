
public final class XPoly {
	public static final double sum(double x, double y){
		return x + y;
	}
	
	public static final double min(double x, double y){
		return Math.min(x, y);
	}
	
	public static final double max(double x, double y){
		return Math.max(x, y);
	}
	
	public static final String TOUPPERFIRSTCHAR(String s) {
		String[] words = s.split(" ");
		String str = "";
		for (String string : words) {	
			str += string.substring(0, 1).toUpperCase() + string.substring(1,string.length()) + " ";
		}
		return str;
	}
}
